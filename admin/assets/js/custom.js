(function ($) {
    "use strict";
    var mainApp = {

        main_fun: function () {
           
            /*====================================
              LOAD APPROPRIATE MENU BAR
           ======================================*/
            $(window).bind("load resize", function () {
                if ($(this).width() < 768) {
                    $('div.sidebar-collapse').addClass('collapse')
                } else {
                    $('div.sidebar-collapse').removeClass('collapse')
                }
            });

          
     
        },

        initialization: function () {
            mainApp.main_fun();

        }

    }
    // Initializing ///

    $(document).ready(function () {
        mainApp.main_fun();
    });

}(jQuery));
(function ($){
    $('#provider').click(function(){
        $('.under_menu_provider').toggle();
        $('.under_menu_provider').addClass('toggle');
        $('.under_menu_product').removeClass('toggle');
    });
    $('#products').click(function(){
        $('.under_menu_product').toggle();
        $('.under_menu_product').addClass('toggle');
        $('.under_menu_provider').removeClass('toggle')
    })
}(jQuery));